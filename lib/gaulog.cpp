/* GAUSS-LOGARITHMIC QUADRATURE
 *  Table of quadrature points that is exact for integrands of the
 *  form f(x) + g(x)*log(x), where f and g are polynomials
 *
 * REFERENCES
 *  Crow, Mathematics of Computation 60-201 (1993)
 * 
 * PARAMETERS
 *  n	[input]		number of points
 *  X	[output]	abcsissas
 *  W	[output]	weights
 */

/* HEADER FILES */
#include "gaulog.h"

/* IMPLEMENTATIONS */
/* Get abscissas X and weights W for an nth order
 * Gauss-logarithmic quadrature on the interval [0, 1] */
void gaulog(int n, double * X, double * W){
	if (n > 5 || n < 1){
		printf("Error: n must be an integer between 1 and 5 (inclusive).\n");
		return;
	}

	if (n == 1){
		X[0] = 0.25;

		W[0] = 1.00;
	}

	if (n == 2){
		X[0] = 0.1120088062;
		X[1] = 0.6022769081;

		W[0] = 0.7185393190;
		W[1] = 0.2814606809;
	}

	if (n == 3){
		X[0] = 0.06389079308;
		X[1] = 0.3689970637;
		X[2] = 0.7668803039;

		W[0] = 0.5134045522;
		W[1] = 0.3919800412;
		W[2] = 0.09461540656;
	}

	if (n == 4){
		X[0] = 0.04144848019938322;
		X[1] = 0.24527491432060225;
		X[2] = 0.55616545356027584;
		X[3] = 0.84898239453298517;

		W[0] = 0.38346406814513512;
		W[1] = 0.38687531777476263;
		W[2] = 0.19043512695014242;
		W[3] = 0.03922548712995983;
	}

	if (n == 5){
		X[0] = 0.029134472151972;
		X[1] = 0.1739772133208976287;
		X[2] = 0.4117025202849020;
		X[3] = 0.677314174582820380;
		X[4] = 0.89477136103100828363;

		W[0] = 0.2978934717828944;
		W[1] = 0.34977622651322418037;
		W[2] = 0.23448829004405241888;
		W[3] = 0.098930459516633146976;
		W[4] = 0.018911552143195796489;
	}
}


